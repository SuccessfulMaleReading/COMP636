# this is a short form factor for writing if else statements

# [code] if condition else [code]

# example
bonus = 1000
# create variable bonus

extra_pay = bonus * 1.5 if bonus > 300 else bonus * 2
print(extra_pay)

# if bonus = 100, that means its less than 300 so the else statement will run extra pay =  200
# if bonus = 1000, that means its more than 300 so the if statement will run extra pay = 1500